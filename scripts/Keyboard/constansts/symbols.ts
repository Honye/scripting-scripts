function getLetters(letters: string, font: string) {
  if (font === "Standard") {
    return letters.split("")
  }

  return letters
    .split("")
    .map((char) => {
      const map: any = Alphabet[font as keyof typeof Alphabet]
      return map[char] || map[char.toUpperCase()] || char
    })
}

export function getLettersInRow1(isUpperCase: boolean, font: string): string[] {
  return getLetters(isUpperCase ? "QWERTYUIOP" : "qwertyuiop", font)
}

export function getLettersInRow2(isUpperCase: boolean, font: string) {
  return getLetters(isUpperCase ? "ASDFGHJKL" : "asdfghjkl", font)
}

export function getLettersInRow3(isUpperCase: boolean, font: string) {
  return getLetters(isUpperCase ? "ZXCVBNM" : "zxcvbnm", font)
}

export function getNumbers(font: string) {
  return getLetters("1234567890", font)
}
